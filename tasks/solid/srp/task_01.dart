// SOLID - Single Responsibility Principle (Принцип единственной ответственности)
// Задача 1: Класс User делает слишком много
//
// ПРИНЦИП:
// Каждый класс должен иметь только одну причину для изменения.
// Класс должен отвечать только за одну задачу.
//
// ПРОБЛЕМА В КОДЕ:
// Класс User отвечает и за данные пользователя, и за сохранение в БД,
// и за отправку email. Это нарушает SRP.
//
// ЗАДАНИЕ:
// Разделите класс User на несколько классов, каждый с одной ответственностью:
// - User - только данные пользователя
// - UserRepository - сохранение в БД
// - EmailService - отправка email
//
// Убедитесь, что код в main() продолжает работать после рефакторинга.

// === КОД С НАРУШЕНИЕМ SRP ===

class User {
  String name;
  String email;

  User(this.name, this.email);

  // Ответственность 1: Данные пользователя
  String getInfo() {
    return 'Пользователь: $name, Email: $email';
  }

  // Ответственность 2: Работа с базой данных
  void saveToDatabase() {
    print('Сохранение пользователя $name в базу данных...');
    // Логика сохранения в БД
  }

  // Ответственность 3: Отправка email
  void sendWelcomeEmail() {
    print('Отправка приветственного email на $email...');
    // Логика отправки email
  }
}

void main() {
  var user = User('Иван', 'ivan@example.com');

  print(user.getInfo());
  user.saveToDatabase();
  user.sendWelcomeEmail();
}

